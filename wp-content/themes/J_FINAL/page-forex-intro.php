    <h4>贸易和投资</h4>
    <p>进出口商在进口商品时支付一种货币，而在出口商品时收取另一种货币。这意味着，它们在结清账目时，收付不同的货币。因此，他们需要将自己收到的部分货币兑换成可以用于购买商品的货币。与此相类似，一家买进外国资产的公司必须用当事国的货币支付，因此，它需要将本国货币兑换成当事国的货币。</p>
    <h4>投机</h4>
    <p>两种货币之间的汇率会随着这两种货币之间的供需的变化而变化。交易员在一个汇率上买进一种货币，而在另一个更有利的汇率上抛出该货币，他就可以盈利。投机大约占了外汇市场交易的绝大部分。</p>
    <h4>对冲</h4>
    <p>由于两种相关货币之间汇率的波动，那些拥有国外资产（如工厂）的公司将这些资产折算成本国货币时，就可能遭受一些风险。当以外币计值的国外资产在一段时间内价值不变时，如果汇率发生变化，以国内货币折算这项资产的价值时，就会产生损益。公司可以通过对冲消除这种潜在的损益。这就是执行一项外汇交易，其交易结果刚好抵消由汇率变动而产生的外币资产的损益。</p>
